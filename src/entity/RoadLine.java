package entity;

public class RoadLine {
	private double length;
	private double weight;
	public RoadLine() {
		length=0.0;
		weight=0.0;
	}
	public double getLength() {
		return length;
	}
	public void setLength(double length) {
		this.length = length;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
}
